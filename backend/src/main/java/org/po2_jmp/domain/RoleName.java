package org.po2_jmp.domain;

/**
 * Enum representing the roles in the system.
 * <p>
 * The {@link RoleName} enum defines two roles for users:
 * <ul>
 *     <li>{@code USER} - Represents a standard user of
 *          the system.</li>
 *     <li>{@code ADMIN} - Represents an administrator with
 *          all privileges.</li>
 * </ul>
 */
public enum RoleName {

    USER,
    ADMIN

}
